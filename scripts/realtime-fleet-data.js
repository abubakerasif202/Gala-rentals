const REGISTRATION_ALIASES = {
  DC95MA: 'DC95AM',
};

const normalizeText = (value) => String(value || '').replace(/\s+/g, ' ').trim();

export const canonicalizeRegistration = (registration) => {
  const normalized = normalizeText(registration).toUpperCase();
  return REGISTRATION_ALIASES[normalized] || normalized;
};

export const getFleetMakeLabel = (makeCode) => MAKE_LABELS[makeCode] || makeCode;

export const getFleetModelLabel = (modelCode) => MODEL_LABELS[modelCode] || modelCode;

export const isAssignedFleetRow = (row) => {
  const driverName = normalizeText(row.driverName);
  return Boolean(driverName) && driverName !== PLACEHOLDER_DRIVER_NAME;
};

export const getFleetCarStatus = (row) => (isAssignedFleetRow(row) ? 'Rented' : 'Available');

export const getFleetCarName = (row) =>
  `${getFleetMakeLabel(row.makeCode)} ${getFleetModelLabel(row.modelCode)} (${canonicalizeRegistration(
    row.registration
  )})`;

export const getFleetCarImage = (row) =>
  `/car-images/${canonicalizeRegistration(row.registration)}.jpeg`;

export const getFleetApplicationEmail = (row) =>
  `legacy-${canonicalizeRegistration(row.registration).toLowerCase()}@example.invalid`;

export const buildFleetCarSeedRows = () =>
  REALTIME_FLEET_ROWS.map((row) => ({
    name: getFleetCarName(row),
    model_year: row.modelYear ?? DEFAULT_MODEL_YEAR,
    weekly_price: row.weeklyRate,
    bond: DEFAULT_CAR_BOND,
    status: getFleetCarStatus(row),
    image: getFleetCarImage(row),
  }));

export const buildFleetDriverSeedRows = ({ carIdByRegistration, importDate, importTimestamp }) => {
  const applications = [];
  const rentals = [];

  for (const row of REALTIME_FLEET_ROWS) {
    if (!isAssignedFleetRow(row)) {
      continue;
    }

    const registration = canonicalizeRegistration(row.registration);
    const carId = carIdByRegistration.get(registration);

    if (!carId) {
      throw new Error(`Missing vehicle id for registration ${registration}`);
    }

    const noteSuffix = row.notes ? ` Notes: ${row.notes}.` : '';
    const experience = `Imported from live fleet data on ${importDate}.${noteSuffix}`;

    applications.push({
      name: normalizeText(row.driverName),
      phone: LEGACY_IMPORT_PHONE,
      email: getFleetApplicationEmail(row),
      license_number: `LEGACY-${registration}`,
      license_expiry: LEGACY_IMPORT_LICENSE_EXPIRY,
      uber_status: LEGACY_IMPORT_UBER_STATUS,
      experience,
      address: LEGACY_IMPORT_ADDRESS,
      weekly_budget: `$${row.weeklyRate}`,
      intended_start_date: importDate,
      status: 'Paid',
      assigned_car_id: carId,
      approved_bond: DEFAULT_CAR_BOND,
      approved_weekly_price: row.weeklyRate,
      approved_at: importTimestamp,
      paid_at: importTimestamp,
      payment_link_version: 0,
      payment_link_sent_at: null,
      pending_checkout_session_id: null,
    });

    rentals.push({
      registration,
      start_date: importDate,
      weekly_price: row.weeklyRate,
      bond_paid: 0,
      status: 'Active',
    });
  }

  return { applications, rentals };
};
